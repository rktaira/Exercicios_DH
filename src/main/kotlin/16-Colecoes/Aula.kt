package `16-Colecoes`
/*

List: Aceita repetições e a ordem importa, como uma frase. Cada item tem seu index, [0, 1, 2, 3...].

Set: Coleção de elementos únicos, onde não importa a ordem. Não pode haver itens duplicados e eles não são indexados.

Map: Pares ordenados de dados, como um nome e idade de um mesmo objeto. NÃO são indexados

Não é recomendado fazer uma coleção de tipo any misturando tipos diferentes

Coleções do tipo "Mutable" (MutableList, MutableSet, MutableMap) aceitam alteração enquanto o programa é executado



 */